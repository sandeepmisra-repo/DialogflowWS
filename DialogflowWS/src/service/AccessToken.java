package service;

public class AccessToken {
	public String Id;
	public String BUnit;
	public String Token;

	public String getId() {
		return Id;
	}

	public void setId(String Id) {
		this.Id = Id;
	}

	public String getBUnit() {
		return BUnit;
	}

	public void setBUnit(String BUnit) {
		this.BUnit = BUnit;
	}

	public String getToken() {
		return Token;
	}

	public void setToken(String Token) {
		this.Token = Token;
	}
}

